"""
Built-in registries
"""


class SysRegistry:
    """
    Structure of all built-in registry names
    """

    # ConfigRegistry
    Configs = "configs"

    # LocaleRegistry
    Locales = "locales"

    # ThemeRegistry
    Themes = "assets"

    # ShortcutsRegistry
    Shortcuts = "shortcuts"

    # PluginRegistry
    Plugins = "plugins"

    # ConfigPageRegistry
    ConfigPages = "configpages"

    # LayoutRegistry
    Layout = "layout"

    # FileSnapshotRegistry
    Snapshots = "snapshots"

    # MenuRegistry
    Menus = "menus"

    # ToolBarRegistry
    ToolBars = "toolbars"

    # TabsRegistry
    Tabs = "tabs"

    # ToolButtonRegistry
    ToolButton = "toolbuttons"

    # QuickActionRegistry
    QuickAction = "quickactions"
